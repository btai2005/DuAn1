    private void btnXacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXacNhanActionPerformed

        if (!txtMa.getText().trim().isEmpty() && !txtGiam.getText().trim().isEmpty()) {
            if (tblVoucher.getSelectedRow() != -1) {
                if (txtMa.getText().trim().equals(tblVoucher.getValueAt(tblVoucher.getSelectedRow(), 1).toString())
                    && txtTen.getText().trim().equals(tblVoucher.getValueAt(tblVoucher.getSelectedRow(), 2).toString())
                    && txtGiam.getText().trim().equals(tblVoucher.getValueAt(tblVoucher.getSelectedRow(), 4).toString())) {

                    ma.setText(txtMa.getText());
                    if (cboLoai.getSelectedIndex() == 0) {
                        giam.setText(txtGiam.getText().trim());
                    } else {
                        giam.setText(txtGiam.getText().trim());
                    }
                    qLy.suaVoucherHoaDon(qLy.getAll(txtSearch.getText(), cboSearch.getSelectedItem().toString()).get(tblVoucher.getSelectedRow()).getId(), idHd);

                    frame.dispose();
                    JOptionPane.showMessageDialog(this,"Chọn Voucher thành công");
                } else {
                    JOptionPane.showMessageDialog(this,"Thông tin Voucher không tồn tại");
                }
            } else {
                JOptionPane.showMessageDialog(this,"Chưa chọn Voucher");
            }
        } else frame.dispose();
    }//GEN-LAST:event_btnXacNhanActionPerformed

